import cvxpy as cp

w = cp.Variable(2)
d = cp.Variable()

probconst = ([2*w[0]+w[1]+d>=1,0.8*w[0]-0.6*w[1]+d<=-1])
probobj = cp.Minimize(0.5*cp.square(cp.norm(w)))

prob = cp.Problem(probobj,probconst)
prob.solve()

print (prob.value)
print (w.value)
print (d.value)





































































































































































































